public class Livro
{
    public string titulo;
    public string nomedoautor;
    public string anolancamento;
    public bool status;
    public int codigoidentificador;

    public Livro(string titulo, string nomedoautor, string anolancamento, bool status, int codigoidentificador)
    {
        this.titulo = titulo;
        this.nomedoautor = nomedoautor;
        this.anolancamento = anolancamento;
        this.anolancamento = anolancamento;
        this.status = status;
        this.codigoidentificador = codigoidentificador;
    }
}